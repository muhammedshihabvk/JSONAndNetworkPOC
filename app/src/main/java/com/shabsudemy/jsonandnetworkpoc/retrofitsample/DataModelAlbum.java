package com.shabsudemy.jsonandnetworkpoc.retrofitsample;

import com.google.gson.annotations.SerializedName;

public class DataModelAlbum {
    private int albumId;
    private int id;
    private String title;
    private String url;

    @SerializedName("thumbnailUrl")
    private String smallUrl;

    public DataModelAlbum() {

    }

    public DataModelAlbum(int albumId, int id, String title, String url, String thumbnailUrl) {
        this.albumId = albumId;
        this.id = id;
        this.title = title;
        this.url = url;
        this.smallUrl = thumbnailUrl;
    }

    public int getAlbumId() {
        return albumId;
    }

    public void setAlbumId(int albumId) {
        this.albumId = albumId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getSmallUrl() {
        return smallUrl;
    }

    public void setSmallUrl(String smallUrl) {
        this.smallUrl = smallUrl;
    }
}
